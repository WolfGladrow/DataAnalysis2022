# CDFs of F distributions
x = seq(0.01,10,0.01)
nu1 = 1; nu2 = 1; X = nu1*x/(nu1*x+nu2); ibeta11 = pbeta(X,nu1/2,nu2/2)
nu1 = 5; nu2 = 1; X = nu1*x/(nu1*x+nu2); ibeta51 = pbeta(X,nu1/2,nu2/2)
nu1 = 1; nu2 = 5; X = nu1*x/(nu1*x+nu2); ibeta15 = pbeta(X,nu1/2,nu2/2)
nu1 = 80; nu2 = 75; X = nu1*x/(nu1*x+nu2); ibeta8075 = pbeta(X,nu1/2,nu2/2)
library(latex2exp)
# png('F-CDF-220228.png',width=16,height=16,units='cm',res=300)
plot(x,ibeta8075,type='l',xlab='x',ylab=NA,col='magenta',lwd=3,las=1,cex.lab=1.5) 
title(ylab=TeX('$CDF \\, of \\, F(x; \\nu_1, \\nu_2)$'),cex.lab=1.5,line=2.3)
lines(x,ibeta11,col='blue',lwd=3,lty=2)
lines(x,ibeta51,col='red',lwd=3,lty=3)
lines(x,ibeta15,col='black',lwd=3,lty=4)
xt = 5.0
text(xt,0.3,TeX('$\\nu_1 = 1,\\, \\nu_2 = 5$'),col='black',cex=1.5)
text(xt,0.1,TeX('$\\nu_1 = 1,\\, \\nu_2 = 1$'),col='blue',cex=1.5)
text(xt,0.2,TeX('$\\nu_1 = 5,\\, \\nu_2 = 1$'),col='red',cex=1.5)
text(xt,0.4,TeX('$\\nu_1 = 80,\\, \\nu_2 = 75$'),col='magenta',cex=1.5)
